package daewooInfo.rss.bean;

import java.io.Serializable;

public class RssKeywordVO implements Serializable {
	
	/**
	 * 키워드 번호
	 * @uml.property  name="keywordId"
	 */
	private String    keywordId;
	/**
	 * 키워드
	 * @uml.property  name="keywordNm"
	 */
	private String keywordNm;
	/**
	 * 상태
	 * @uml.property  name="status"
	 */
	private String status;
	/**
	 * 등록자
	 * @uml.property  name="regId"
	 */
	private String regId;
	/**
	 * 등록일자
	 * @uml.property  name="regDate"
	 */
	private String regDate;
	
	/**
	 * 등록시간
	 * @uml.property  name="regTime"
	 */
	private String regTime;
	
	/**
	 * 체크
	 * @uml.property  name="checkNo"
	 */
	private String[] checkNo;
	
	
	
	public String getKeywordId() {
		return keywordId;
	}
	public void setKeywordId(String keywordId) {
		this.keywordId = keywordId;
	}
	public String getKeywordNm() {
		return keywordNm;
	}
	public void setKeywordNm(String keywordNm) {
		this.keywordNm = keywordNm;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getRegId() {
		return regId;
	}
	public void setRegId(String regId) {
		this.regId = regId;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	public String getRegTime() {
		return regTime;
	}
	public void setRegTime(String regTime) {
		this.regTime = regTime;
	}
	public String[] getCheckNo() {
		return checkNo;
	}
	public void setCheckNo(String[] checkNo) {
		this.checkNo = checkNo;
	}

	
}